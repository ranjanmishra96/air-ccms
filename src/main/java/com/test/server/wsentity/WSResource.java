package com.test.server.wsentity;

public class WSResource  {
	
	private Long id;
	private WSNameValue resource;
	private Boolean active;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public WSNameValue getResource() {
		return resource;
	}
	public void setResource(WSNameValue resource) {
		this.resource = resource;
	}
	public Boolean getActive() {
		return active;
	}
	public void setActive(Boolean active) {
		this.active = active;
	}
	
	
	

}
